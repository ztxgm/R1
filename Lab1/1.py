print("\n№ 8")
cnt, mn, mx = 0, 200, 0
while True:
    s = input()
    if s == '!': break
    h = int(s)
    if 150 <= h <= 190:
        cnt += 1
        if h < mn: mn = h
        if h > mx: mx = h
print(cnt)
if cnt: print(mn, mx)


