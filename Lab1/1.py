'''Напишите программу, которая имитирует проверку пароля, придуманного пользователем.
Пользователь вводит пару слов: пароль, а потом ещё раз его же, для подтверждения.
Если введённая пара не удовлетворяет одному из перечисленных ниже условий,
пользователь вводит пару паролей ещё раз, и так до тех пор, пока не будут выполнены все
условия (т. е. пока программа не выведет «OK»).
    - если первый пароль из пары, который ввёл пользователь короче 8 символов,
программа выводит на экран слово "Короткий!" и заново считывает пару слов-
паролей;
    - если же первый пароль из пары достаточно длинный, но в нём содержится сочетание
символов "123", программа выводит на экран слово "Простой!" и снова читает пару
слов-паролей;
    - если же предыдущие проверки пройдены успешно, но введённые слова-пароли не
совпадают, то программа выводит на экран слово "Различаются." и опять же читает
пару слов-паролей;
    - если же и третья проверка пройдена успешно, программа выводит "OK" (латинскими
буквами) и заканчивает свою работу.'''
print("\n№ 9")
while True:
    p1, p2 = input(), input()
    if len(p1) < 8:
        print('Короткий!')
    elif '123' in p1:
        print('Простой!')
    elif p1 != p2:
        print('Различаются.')
    else:
        print('OK')
        break


# № 10
'''Напишите калькулятор для целочисленных вычислений, который обрабатывает серию
команд и выводит результат вычислений по каждой из них.
Каждая команда записывается так: сначала вводится первое число, затем символ операции,
затем — при необходимости — второе число. Символы операций (гарантируется, что
используются только эти символы):
    «+» (сложить два числа),
    «-» (вычесть из первого числа второе),
    «*» (перемножить два числа),
    «/» (поделить первое число на второе нацело),
    «%» (получить остаток от деления первого числа на второе),
    «!» (посчитать факториал от первого числа, ввод второго числа не производится),
    «x» (вывести первое число и закончить выполнение программы, ввод второго числа
не производится).

Если пользователь попытается взять факториал от отрицательного числа или сделать ещё
что-то незаконное, то никакой результат для этой команды выводить не следует.'''
print("\n№ 10")
while True:
    try:
        line = input().strip()
        if not line:
            continue
        a = int(line)
        op = input().strip()
        if op == 'x':
            print(a)
            break
        elif op == '!':
            if a < 0:
                continue
            res = 1
            for i in range(2, a + 1):
                res *= i
            print(res)
        else:
            b = int(input().strip())
            if op == '+':
                print(a + b)
            elif op == '-':
                print(a - b)
            elif op == '*':
                print(a * b)
            elif op == '/':
                if b != 0:
                    print(a // b)
            elif op == '%':
                if b != 0:
                    print(a % b)
    except EOFError:
        break
    except ValueError:
        break
    except Exception:
        break


